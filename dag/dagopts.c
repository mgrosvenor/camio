/* dagoptlex_destroy is for both reentrant and non-reentrant scanners. */
int dagoptlex_destroy  (void)
{
    
    /* Pop the buffer stack, destroying each element. */
	while(YY_CURRENT_BUFFER){
		dagopt_delete_buffer(YY_CURRENT_BUFFER  );
		YY_CURRENT_BUFFER_LVALUE = NULL;
		dagoptpop_buffer_state();
	}

	/* Destroy the stack itself. */
	dagoptfree((yy_buffer_stack) );
	(yy_buffer_stack) = NULL;

    /* Reset the globals. This is important in a non-reentrant scanner so the next time
     * dagoptlex() is called, initialization will occur. */
    yy_init_globals( );

    return 0;
}

